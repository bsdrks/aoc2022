fn solve(s: &str) -> usize {
    let mut starts = vec![];
    let mut end: (usize, usize) = (0, 0);

    let grid = s
        .lines()
        .enumerate()
        .map(|(y, l)| {
            l.chars()
                .enumerate()
                .map(|(x, c)| match c {
                    'S' | 'a' => {
                        starts.push((x, y));
                        0
                    }
                    'E' => {
                        end = (x, y);
                        25
                    }
                    _ => c as usize - 'a' as usize,
                })
                .collect::<Vec<_>>()
        })
        .collect::<Vec<_>>();

    let w = grid[0].len();
    let h = grid.len();
    let mut distances = vec![vec![usize::MAX; w]; h];

    for start in starts {
        dfs(&grid, &mut distances, (w - 1, h - 1), start, end, 0);
    }

    distances[end.1][end.0]
}

fn dfs(
    h: &[Vec<usize>],
    d: &mut Vec<Vec<usize>>,
    max: (usize, usize),
    pos: (usize, usize),
    end: (usize, usize),
    steps: usize,
) {
    if d[pos.1][pos.0] <= steps {
        return;
    }

    d[pos.1][pos.0] = steps;

    if pos == end {
        return;
    }

    let steps = steps + 1;
    let (x, y) = pos;
    let h_required = h[y][x] + 1;

    if x > 0 && h[y][x - 1] <= h_required {
        dfs(h, d, max, (x - 1, y), end, steps);
    }

    if x < max.0 && h[y][x + 1] <= h_required {
        dfs(h, d, max, (x + 1, y), end, steps);
    }

    if y > 0 && h[y - 1][x] <= h_required {
        dfs(h, d, max, (x, y - 1), end, steps);
    }

    if y < max.1 && h[y + 1][x] <= h_required {
        dfs(h, d, max, (x, y + 1), end, steps);
    }
}

